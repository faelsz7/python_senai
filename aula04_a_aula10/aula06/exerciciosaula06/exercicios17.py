# Solicita os números ao usuário
numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))

soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

print(f"A some de {numero1} e {numero2} é {soma}")

print(f"A subtracao de {numero1} e {numero2} é {subtracao}")

print(f"A multiplicacao de {numero1} e {numero2} é {multiplicacao}")

print(f"A divisao de {numero1} e {numero2} é {divisao}")